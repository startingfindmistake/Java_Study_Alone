package chapter2;

public class BooleanExample {
    public static void main(String[] args) {
        boolean stop = true; // boolean 타입 변수 stop 선언 및 초기화
        if (stop) { // stop 변수가 true일 때 실행
            System.out.println("중지합니다.");
        } else { // stop 변수가 false일 때 실행
            System.out.println("시작합니다.");
        }
    }    
}
